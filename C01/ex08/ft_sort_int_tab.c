void	ft_sort_int_tab(int *tab, int size)
{
	int	i;
	int	temp;
	int	j;

	temp = 0;
	i = 0;
	while (i < size)
	{
		j = 0;
		while (j < size - 1)
		{
			if (tab[i] < tab[j])
			{
				temp = tab[i];
				tab[i] = tab[j];
				tab[j] = temp;
			}
			j++;
		}
		i++;
	}
}
